const loadPolicies = () => {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved) return { ...DEFAULT_POLICIES, ...JSON.parse(saved) };
  } catch { /* ignore */ }
  return DEFAULT_POLICIES;
};

const PolicyPage = () => {
  const { type } = useParams();
  const navigate = useNavigate();
  const [policies] = useState(loadPolicies);

  const policy = policies[type];

  if (!policy) {
    return (
      <Box sx={{ textAlign: 'center', py: 8 }}>
        <Typography variant="h6" color="text.secondary">페이지를 찾을 수 없습니다.</Typography>
        <Button sx={{ mt: 2 }} onClick={() => navigate(-1)}>돌아가기</Button>
      </Box>
    );
  }

  return (
    <Box sx={{ maxWidth: 900, mx: 'auto', py: { xs: 3, md: 5 }, px: { xs: 2, sm: 3 } }}>
      <Button startIcon={<BackIcon />} onClick={() => navigate(-1)}
        sx={{ mb: 3, color: 'text.secondary' }}>
        돌아가기
      </Button>

      <Paper elevation={0} sx={{ p: { xs: 3, md: 5 }, border: '1px solid', borderColor: 'divider', borderRadius: '12px' }}>
        <Typography variant="h4" fontWeight={700} sx={{ mb: 1 }}>
          {policy.title}
        </Typography>
        <Typography variant="body2" color="text.secondary" sx={{ mb: 4 }}>
          최종 수정일: {policy.updatedAt}
        </Typography>

        <Box sx={{
          '& p': { mb: 0 },
          whiteSpace: 'pre-wrap',
          lineHeight: 1.9,
          fontSize: '0.92rem',
          color: '#333',
        }}>
          {policy.content}
        </Box>
      </Paper>
    </Box>
  );
};

